-- Donnees INSEE par commune (pour la fiche d'etude de marche Elizabeth Holding).
-- population : pre-remplie (geo.api.gouv.fr, population legale).
-- Les autres indicateurs (proprietaires/locataires, revenu median, evolution,
-- logements recents) sont a enrichir depuis les fichiers INSEE -> "n.c." sinon.
create table if not exists public.insee_communes (
  insee text primary key,
  ville text,
  pct_proprietaires numeric,
  pct_locataires numeric,
  population integer,
  evolution_pop numeric,
  revenu_median integer,
  pct_logements_recents numeric,
  updated_at timestamptz not null default now()
);

alter table public.insee_communes enable row level security;

-- Lecture publique (le front lit cette table avec la cle publishable)
drop policy if exists "insee lecture publique" on public.insee_communes;
create policy "insee lecture publique" on public.insee_communes for select using (true);

